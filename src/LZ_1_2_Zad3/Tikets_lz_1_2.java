package LZ_1_2_Zad3;

public class Tikets_lz_1_2 {
    public static void main(String[] args) {
        int count = 0; // Здесь будем считать количество счастливых билетов

        // Проходим по всем номерам билетов от 000001 до 999999
        for (int ticket = 1; ticket <= 999999; ticket++) {
            int sumLastThree = 0;  // Сумма последних трёх цифр
            int sumFirstThree = 0; // Сумма первых трёх цифр

            int temp = ticket; // Копируем номер билета, чтобы с ним работать

            // Сначала считаем сумму последних трёх цифр (правые цифры)
            for (int i = 0; i < 3; i++) {
                sumLastThree += temp % 10; // Берём последнюю цифру и прибавляем к сумме
                temp /= 10;                 // Убираем последнюю цифру
            }

            // Теперь считаем сумму первых трёх цифр (левые цифры)
            for (int i = 0; i < 3; i++) {
                sumFirstThree += temp % 10; // Берём следующую цифру и прибавляем к другой сумме
                temp /= 10;                 // Убираем цифру
            }

            // Если суммы равны — билет "счастливый"
            if (sumFirstThree == sumLastThree) {
                count++; // Увеличиваем счётчик счастливых билетов
            }
        }

        // Выводим общее количество счастливых билетов
        System.out.println("Количество счастливых билетов: " + count);
    }
}